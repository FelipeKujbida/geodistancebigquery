WITH COORDINATES_ORIGIN_AND_DESTINATION AS(
SELECT 
-23.56126646865952 AS LATITUDE_ORIGIN,
-46.655871173180955 AS LONGITUDE_ORIGIN,
-23.57241924761826 AS LATITUDE_DESTINATION,
-46.69600770201613 AS LONGITUDE_DESTINATION
)

SELECT *,
ROUND (DISTANCE / 1000) AS DISTINCE_KM
FROM(
SELECT *,
st_distance( st_geogpoint(LONGITUDE_ORIGIN, LATITUDE_ORIGIN),
st_geogpoint(LONGITUDE_DESTINATION, LATITUDE_DESTINATION)) AS DISTANCE
FROM COORDINATES_ORIGIN_AND_DESTINATION)
